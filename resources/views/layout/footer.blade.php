	<script type="text/javascript">
		function change_theme(theme){
			
			var url="{{ url('change-theme')}}";
			$.ajax({
				type    : 'POST',
				url     : url,
				data    : {'theme': theme,'_token': '{{csrf_token()}}'},
				dataType: "json",
				success : function(result){
					if (result.notif=='true') {
						$('html').attr('class', theme);
					}
					
	            }
	        });
		}
		
